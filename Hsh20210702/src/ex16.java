public class ex16 {

	public static void main(String[] args) {
		// 2021.07.09. 프로그래밍 -15- While 문

		System.out.print("for 결과>> ");
		for (int i = 1; i < 10; i += 2) {
			System.out.print(i + " ");
		}
		
		System.out.println(" ");
		System.out.print("whlie 결과>> ");
		int j = 1;
		while (j < 10) {
			System.out.print(j + " ");
			j += 2;
		}

	}

}
